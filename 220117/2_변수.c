//					-메모리를 할당하고 해지하는 비용이 없다.				
//					-초기화하지 않으면 쓰레기값
//		전역변수/정적 지역 변수		:데이터영역
//					- 프로그램이 시작될 때 초기화되고, 프로그램이 종료될 때 사라진다.
//					- 값이 계속 유지는 되어야하지만, 특정 함수를 통해서만 접근 가능하도록 만들고 싶음
